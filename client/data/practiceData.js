// Transform puzzle data to game format
const transformPuzzleData = (rawPuzzle) => {
  const wordPoints = {};
  const validWords = rawPuzzle.answers || [];
  
  validWords.forEach(word => {
    const basePoints = word.length * word.length;
    const isPangram = rawPuzzle.pangrams.includes(word);
    const pangramBonus = isPangram ? 25 : 0;
    wordPoints[word.toLowerCase()] = basePoints + pangramBonus;
  });

  return {
    centerLetter: rawPuzzle.centerLetter,
    outerLetters: rawPuzzle.outerLetters,
    validLetters: rawPuzzle.validLetters,
    validWords: validWords.map(word => word.toLowerCase()),
    pangrams: rawPuzzle.pangrams.map(word => word.toLowerCase()),
    wordPoints,
    totalWords: validWords.length,
    totalPangrams: rawPuzzle.pangrams.length,
    displayDate: rawPuzzle.displayDate,
    isPracticeMode: true
  };
};

// Export transformed puzzles ready for use
export const TRANSFORMED_PRACTICE_PUZZLES = PRACTICE_PUZZLES.map(transformPuzzleData);

// Export utility function for manual transformation if needed
export const transformPuzzle = transformPuzzleData;